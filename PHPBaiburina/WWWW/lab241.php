    $N = rand(1,10); // Размер массива

    echo "Изначальный массив: ";
    $a[0] = rand(-10,10);
    echo $a[0];
    for ($c = 1; $c < $N; $c++){
        $a[] = rand(-10,10);
        echo ", ".$a[$c];
    }

