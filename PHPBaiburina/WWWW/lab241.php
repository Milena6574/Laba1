        if ($a[$c] < 0){
            $a1[] = $a[$c];
        }
        else if ($a[$c] == 0){
            $a2[] = $a[$c];
        }
        else{
            $a3[] = $a[$c];
        }
    }

    unset($a); // Отчистка массива

    // Загрузка чисел обратно в массив
    for ($c = 0; $c < count($a1); $c++){
        $a[] = $a1[$c];
    }
    for ($c = 0; $c < count($a2); $c++){
        $a[] = $a2[$c];
    }
